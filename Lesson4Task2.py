# З клавіатури вводиться число (шестизначне). Перевірити, чи воно є паліндромом.
# Примітка: Паліндром називається число, слово або текст, які однаково читаються
# зліва направо і справа наліво. Наприклад, це числа 143341, 5555, 7117 і т.д.

slovo = str(input("Введіть шестизначне число: "))
a = slovo[::-1]
res = "Так, це паліндром." if slovo == a else "Ні, це не паліндром."
print(res)
